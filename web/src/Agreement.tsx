// onAccept → first-run gate (writes acceptance). onClose → read-only view from
// the footer link. Pass one of them.
export function Agreement({
  onAccept,
  onClose,
}: {
  onAccept?: () => void;
  onClose?: () => void;
}) {
  const accept = () => {
    try {
      localStorage.setItem(ACCEPT_KEY, "1");
    } catch {
      /* ignore */
    }
    onAccept?.();
  };

  const footer = onAccept ? (
    <Button onClick={accept} className="w-full sm:w-auto">
      Принимаю
    </Button>
  ) : (
    <Button
      variant="light"
      color="gray"
      onClick={onClose}
      className="w-full sm:w-auto"
    >
      Закрыть
    </Button>
  );

  return (
    <InfoModal
      icon={<IconShield size={22} />}
      title="Пользовательское соглашение"
      onClose={onClose}
      footer={footer}
    >
      {SECTIONS.map((s) => (
        <InfoSection key={s.title} title={s.title}>
          {s.body}
        </InfoSection>
      ))}
      {onAccept && (
        <div className="rounded-lg border border-gray-200 bg-gray-50 p-3 text-center text-xs text-gray-500">
          Нажимая «Принимаю», вы безоговорочно принимаете условия.
        </div>
      )}
    </InfoModal>
  );
}
